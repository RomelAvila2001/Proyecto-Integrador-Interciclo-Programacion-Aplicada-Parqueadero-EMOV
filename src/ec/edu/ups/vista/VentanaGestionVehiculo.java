    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String cedula= txtCedula.getText();
        Cliente cl = new Cliente(cedula);
        Cliente cliente = controladorCliente.read(cl);
        if(cliente!= null){
            txtCodigo2.setText(String.valueOf(cliente.getCodigo()));
            txtNombre.setText(cliente.getNombre());
            txtApellido.setText(cliente.getApellido());
            JDate.setCalendar(cliente.getFechaNacimiento());
        }else{
            int opcion=JOptionPane.showConfirmDialog(this, "Cliente no encontrado desea crear uno"); 
            if(opcion== JOptionPane.YES_OPTION)
            {
                ventanaPrincipal.getDesktopPane().add(ventanaCrearCliente);
                ventanaCrearCliente.setVisible(true);
            }
        }
        
    }//GEN-LAST:event_btnBuscarActionPerformed

