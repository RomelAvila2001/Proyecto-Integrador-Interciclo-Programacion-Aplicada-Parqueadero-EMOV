    public void cargarDatos() {
       DefaultTableModel modeloTabla = (DefaultTableModel) tblVehiculos.getModel();

        modeloTabla.setRowCount(0);
        for (Vehiculo vehiculo : controladorVehiculo.getListaGenerica()) {
            Object[] rowData = {vehiculo.getCodigo(),vehiculo.getColor(), vehiculo.getMarca(),vehiculo.getModelo(),vehiculo.getPlaca(),vehiculo.getCliente()};
            modeloTabla.addRow(rowData);
        }
        tblVehiculos.setModel(modeloTabla);
    }

