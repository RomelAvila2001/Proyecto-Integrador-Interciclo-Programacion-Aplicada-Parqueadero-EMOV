    public int cargarCodigo() {
        int cod = controladorFactura.cargarCodigo();
        //txtCodigo.setText(String.valueOf(cod));
        return cod;
    }
    
    public void setNumero(String numero) {
        this.numero = numero;
    }

    public void CrearFactura(){
        int codigo= cargarCodigo();
        local = LocalDateTime.now();
        String estado="deuda";
        
        Calendar fechaIngreso = Calendar.getInstance();
        Sitio sit= new Sitio(numero);
        Sitio sitio=this.controladorSitio.read(sit);
        Vehiculo vehiculo = sitio.getVehiculo();
        Cliente cliente=vehiculo.getCliente();
        
        Factura factura=new Factura(codigo, fechaIngreso, estado, cliente, sitio);
        boolean respuesta= controladorFactura.create(factura);
        
        String datos = "Tiket numero: "+codigo+"\nFecha y hora de ingreso: "+fechaIngreso.getTime()+"\nCliente: " +cliente.getNombre()+" "+cliente.getApellido()+"\nCedula: "+cliente.getCedula()+"\nPlaca vehiculo: "+vehiculo.getPlaca()+"\nMarca: "+vehiculo.getMarca()+"\nModelo: "+vehiculo.getModelo()+"\nNumero sitio parqueado: "+sitio.getNumero();
        txtDatos.setText(datos);
        JOptionPane.showMessageDialog(this, "Operaci√≥n : " + respuesta);
    }

